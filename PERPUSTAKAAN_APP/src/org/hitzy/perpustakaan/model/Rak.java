package org.hitzy.perpustakaan.model;

public class Rak {

	private int rakId;
	private String namaRak;
	private String segmen;
	
	public int getRakId() {
		return rakId;
	}
	public void setRakId(int rakId) {
		this.rakId = rakId;
	}
	public String getNamaRak() {
		return namaRak;
	}
	public void setNamaRak(String namaRak) {
		this.namaRak = namaRak;
	}
	public String getSegmen() {
		return segmen;
	}
	public void setSegmen(String segmen) {
		this.segmen = segmen;
	}
	
}
